package pkg100.days;

public class day51_ArrayD1 {
    public static void main(String[] args) {
        
        System.out.println("Mencetak array dengan for");
        int[] nilai = {12,2,13,5,4,6};
        for (int i = 0; i < nilai.length; i++) {
            System.out.println(nilai[i]);
        }
    }
    
}
