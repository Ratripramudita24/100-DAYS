package pkg100.days;
public class day40_for {
    public static void main(String[] args) {
        
        int angka = 5;
        for (int i = 1; i <= angka ; i++) {
            for (int j = 1; j <= angka; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
    
}
