
package pkg100.days;

public class day22_perulangan {
    
    public static void main(String[] args) {
        
        System.out.println("menghasilkan angka genap");
        for (int i = 2; i < 35; i+=2) {
            System.out.println("i = " + i);
            
        }
        System.out.println("- - - - - - - - - - - - - - ");
        System.out.println("                            ");
        System.out.println("menghasilkan angka ganjil");
        for (int y = 1; y <= 35; y+=2) {
            System.out.println("Y: "+ y);
            
        }
    }
    
}
